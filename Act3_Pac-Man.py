# Función para dibujar un cuadrado en una posición dada
def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

# Calcula el índice en la lista `tiles` según la posición en el mundo
def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

# Verifica si una posición es válida (no es una pared y está dentro del mundo)
def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

# Dibuja el mundo inicial basado en la configuración de tiles
def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

# Mueve a Pacman y los fantasmas, y verifica colisiones
def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])
    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    # Mueve a cada fantasma hacia Pacman usando un movimiento inteligente
    for point, course in ghosts:
    # Lista de posibles movimientos
        options = [
            vector(5, 0),   # derecha
            vector(-5, 0),  # izquierda
            vector(0, 5),   # arriba
            vector(0, -5),  # abajo
        ]

        # Selecciona el mejor movimiento para acercarse a Pacman
        best_move = None
        best_distance = float('inf')

        for option in options:
            next_pos = point + option
            if valid(next_pos):
                distance = abs(next_pos - pacman)
                if distance < best_distance:
                    best_move = option
                    best_distance = distance

        # Mueve el fantasma a la posición seleccionada
        if best_move is not None:
            point.move(best_move)
        
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    # Verifica si algún fantasma ha alcanzado a Pacman (condición de fin de juego)
    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return     # Verifica si algún fantasma ha alcanzado a Pacman (condición de fin de juego)

    ontimer(move, 40)

# Cambia la dirección de Pacman si la dirección es válida
def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

